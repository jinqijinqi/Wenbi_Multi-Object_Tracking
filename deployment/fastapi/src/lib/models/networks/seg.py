def fill_fc_weights(layers):
    for m in layers.modules():
        if isinstance(m, nn.Conv2d):
            nn.init.normal_(m.weight, std=0.001)
            if m.bias is not None:
                nn.init.constant_(m.bias, 0)


def fill_up_weights(up):
    w = up.weight.data
    f = math.ceil(w.size(2) / 2)
    c = (2 * f - 1 - f % 2) / (2. * f)
    for i in range(w.size(2)):
        for j in range(w.size(3)):
            w[:, 0, i, j] = \
                (1 - math.fabs(i / f - c)) * (1 - math.fabs(j / f - c))


class IDAUp(nn.Module):

    def __init__(self, o, channels, up_f):
        super(IDAUp, self).__init__()
        for i in range(1, len(channels)):
            c = channels[i]
            f = int(up_f[i])
            proj = DeformConv(c, o)
            node = DeformConv(o, o)

            up = nn.ConvTranspose2d(o, o, f * 2, stride=f,
                                    padding=f // 2, output_padding=0,
                                    groups=o, bias=False)
            fill_up_weights(up)

            setattr(self, 'proj_' + str(i), proj)
            setattr(self, 'up_' + str(i), up)
            setattr(self, 'node_' + str(i), node)

    def forward(self, layers, startp, endp):
        for i in range(startp + 1, endp):
            upsample = getattr(self, 'up_' + str(i - startp))
            project = getattr(self, 'proj_' + str(i - startp))
            layers[i] = upsample(project(layers[i]))
            node = getattr(self, 'node_' + str(i - startp))
            layers[i] = node(layers[i] + layers[i - 1])


class DLAUp(nn.Module):
    def __init__(self, startp, channels, scales, in_channels=None):
        super(DLAUp, self).__init__()
        self.startp = startp
        if in_channels is None:
            in_channels = channels
        self.channels = channels
        channels = list(channels)
        scales = np.array(scales, dtype=int)
        for i in range(len(channels) - 1):
            j = -i - 2
            setattr(self, 'ida_{}'.format(i),
                    IDAUp(channels[j], in_channels[j:],
                          scales[j:] // scales[j]))
            scales[j + 1:] = scales[j]
            in_channels[j + 1:] = [channels[j] for _ in channels[j + 1:]]

    def forward(self, layers):
        out = [layers[-1]]  # start with 32
        for i in range(len(layers) - self.startp - 1):
            ida = getattr(self, 'ida_{}'.format(i))
            ida(layers, len(layers) - i - 2, len(layers))
            out.insert(0, layers[-1])
        return out


class PosePVT(nn.Module):
    def __init__(self,num_layers, heads, head_conv):
        super(PosePVT, self).__init__()
        self.backbone = models[num_layers]()
        # state_dict = torch.load("/root/data/lwb/FairMOT/models/%s.pth" % pre[num_layers])
        # self.backbone.load_state_dict(state_dict,strict=False)
        embed_dims=self.backbone.embed_dims
        self.heads = heads
        # used for deconv layers
        self.deconv_layer1 = self._make_deconv_layer(embed_dims[-2], embed_dims[-1], 4)
        self.deconv_layer2 = self._make_deconv_layer(embed_dims[-3], embed_dims[-2], 4)
        self.deconv_layer3 = self._make_deconv_layer(embed_dims[-4], embed_dims[-3], 4)

        self.smooth_layer1 = DeformConv(embed_dims[-2], embed_dims[-2])
        self.smooth_layer2 = DeformConv(embed_dims[-3], embed_dims[-3])
        self.smooth_layer3 = DeformConv(embed_dims[-4], embed_dims[-4])

        self.project_layer1 = DeformConv(embed_dims[-2], embed_dims[-2])
        self.project_layer2 = DeformConv(embed_dims[-3], embed_dims[-3])
        self.project_layer3 = DeformConv(embed_dims[-4], embed_dims[-4])
        self.project_layer4 = DeformConv(embed_dims[-3], embed_dims[0])

        for head in self.heads:
            classes = self.heads[head]
            if head_conv > 0:
                fc = nn.Sequential(
                    nn.Conv2d(embed_dims[0], head_conv,
                              kernel_size=3, padding=1, bias=True),
                    nn.SiLU(inplace=True),
                    nn.Conv2d(head_conv, classes,
                              kernel_size=1, stride=1,
                              padding=0, bias=True))
                if 'hm' in head:
                    fc[-1].bias.data.fill_(-2.19)
                else:
                    fill_fc_weights(fc)
            else:
                fc = nn.Conv2d(embed_dims[0], classes,
                               kernel_size=1, stride=1,
                               padding=0, bias=True)
                if 'hm' in head:
                    fc.bias.data.fill_(-2.19)
                else:
                    fill_fc_weights(fc)
            self.__setattr__(head, fc)

    def _get_deconv_cfg(self, deconv_kernel):
        if deconv_kernel == 4:
            padding = 1
            output_padding = 0
        elif deconv_kernel == 3:
            padding = 1
            output_padding = 1
        elif deconv_kernel == 2:
            padding = 0
            output_padding = 0

        return deconv_kernel, padding, output_padding


    def _make_deconv_layer(self, num_filters, inplanes, num_kernels):

        layers = []

        kernel, padding, output_padding = \
            self._get_deconv_cfg(num_kernels)

        planes = num_filters
        fc = DCN(inplanes, planes,
                 kernel_size=(3, 3), stride=1,
                 padding=1, dilation=1, deformable_groups=1)

        up = nn.ConvTranspose2d(
            in_channels=planes,
            out_channels=planes,
            kernel_size=kernel,
            stride=2,
            padding=padding,
            output_padding=output_padding,
            bias=False)
        fill_up_weights(up)

        layers.append(fc)
        layers.append(nn.BatchNorm2d(planes, momentum=BN_MOMENTUM))
        layers.append(nn.SiLU(inplace=True))
        layers.append(up)
        layers.append(nn.BatchNorm2d(planes, momentum=BN_MOMENTUM))
        layers.append(nn.SiLU(inplace=True))

        return nn.Sequential(*layers)

    def init_weights(self,pretrained):
        if isinstance(pretrained, str):
            load_checkpoint(self, pretrained, map_location='cpu', strict=False)
            print("load pretrain checkpoint")

    def forward_features(self, x):
        outs = self.backbone.forward_features(x)
        c1, c2, c3, c4 = outs  # len=4, 1/4,1/8,1/16,1/32
        # ############## MLP decoder on C1-C4 ###########
        n, _, h, w = c4.shape

        p4 = c4
        p3 = self.smooth_layer1(self.deconv_layer1(p4) + self.project_layer1(c3))
        p2 = self.smooth_layer2(self.deconv_layer2(p3) + self.project_layer2(c2))
        p1 = self.smooth_layer3(self.deconv_layer3(p2) + self.project_layer3(c1))
        return p1

    def forward(self, x):
        x = self.forward_features(x)
        ret = {}
        for head in self.heads:
            ret[head] = self.__getattr__(head)(x)
        return [ret]


